#include <iostream>
using namespace std;

int main() {
  // variable
  // int 
  int a = 10, b = 56;
  cout << a + b << endl;

  // char
  char c = 'a';
  cout << c << endl;
  
  // float 
  float ft = 4.55;
  cout << ft << endl;

  // double 
  double db = 45.6963;
  cout << db << endl;

  // boolean
  bool bl = true;
  cout << bl << endl;

  return 0;
}