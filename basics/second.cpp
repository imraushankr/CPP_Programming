#include <iostream>
using namespace std;

int main() {
  // variable
  // int 
  int a, b;
  cin >> a >> b;
  cout << a + b << endl;

  // char
  char c;
  cin >> c;
  cout << c << endl;
  
  // float 
  float ft;
  cin >> ft;
  cout << ft << endl;

  // double 
  double db;
  cin >> db;
  cout << db << endl;

  // boolean
  bool bl;
  cin >> bl;
  cout << bl << endl;

  return 0;
}